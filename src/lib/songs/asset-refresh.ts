import { prisma } from "@/lib/prisma";
import { fetchFreshUrls } from "@/lib/sunoapi";
import { sunoCdnAudioUrl } from "@/lib/sunoapi/mappers";
import { logger } from "@/lib/logger";
import { logServerError } from "@/lib/error-logger";
import {
  CDN_URL_TTL_MS,
  PERMANENT_CDN_HOST,
  isPermanentCdnUrl,
} from "@/lib/cdn-constants";

/** Hostname of a URL for event tags; undefined when unparsable/absent. */
function urlHost(url: string | null | undefined): string | undefined {
  if (!url) return undefined;
  try {
    return new URL(url).hostname;
  } catch {
    return undefined;
  }
}

/**
 * Minimal song projection needed to refresh its CDN assets. Matches the
 * columns every existing refresh block selects (audio proxy, image proxy,
 * play/refresh routes, cache warmup).
 */
export interface RefreshableSong {
  id: string;
  sunoJobId: string | null;
  sunoAudioId: string | null;
  /**
   * When true the cover was uploaded/generated by the user — the
   * Suno-provided imageUrl must never overwrite it. Omitted/false means the
   * cover is Suno's own and may be healed.
   */
  imageUrlIsCustom?: boolean;
  /**
   * Parent song's sunoJobId. For alternates, the local `sunoJobId` is a
   * clip-UUID, not a real Suno task-id, so record-info lookups must be
   * keyed by the parent's task-id instead.
   */
  parentSunoJobId?: string | null;
}

export interface AssetRefreshDeps {
  resolveApiKey: () => Promise<string | undefined>;
}

export interface RefreshedCdnUrls {
  /** Absent when the caller opted out of the audio heal (healAudio: false). */
  audioUrl?: string;
  /**
   * Absent when Suno returned no image OR the song has a custom cover —
   * callers can use whatever comes back without re-checking the guard.
   */
  imageUrl?: string;
}

export interface AssetRefreshOptions {
  /**
   * Write the audio columns as part of the heal (default true). The image
   * proxy passes false: a cover request must never rewrite audioUrl — the
   * aggregator can hand back the SAME dead tempfile URL and would regress a
   * row already healed to the permanent cdn1 derivation (incident
   * 2026-07-17).
   */
  healAudio?: boolean;
}

/**
 * Refresh a song's CDN URLs from the Suno aggregator and heal the DB row.
 *
 * Owns the shared refresh recipe: task-id resolution (parent's for
 * alternates), the fetchFreshUrls call, and the heal-write of
 * audioUrl/audioUrlExpiresAt plus imageUrl/imageUrlExpiresAt — the image
 * write is guarded by `imageUrlIsCustom` so a custom cover is never
 * overwritten.
 *
 * Never throws. Returns the fresh URLs on success, null when there is no
 * task-id, the aggregator has no audioUrl, or any step fails (logged).
 */
export async function refreshSongCdnUrls(
  song: RefreshableSong,
  deps: AssetRefreshDeps,
  opts: AssetRefreshOptions = {},
): Promise<RefreshedCdnUrls | null> {
  const healAudio = opts.healAudio !== false;
  const refreshTaskId = song.parentSunoJobId ?? song.sunoJobId;
  if (!refreshTaskId) return null;

  try {
    const apiKey = await deps.resolveApiKey();
    const fresh = await fetchFreshUrls(
      refreshTaskId,
      apiKey,
      song.sunoAudioId ?? undefined,
    );
    const expiresAt = new Date(Date.now() + CDN_URL_TTL_MS);
    const freshImageUrl = song.imageUrlIsCustom
      ? undefined
      : fresh?.imageUrl || undefined;

    if (healAudio && !fresh?.audioUrl) {
      logger.warn(
        { songId: song.id, refreshTaskId },
        "asset-refresh: refresh returned no audioUrl",
      );
      return null;
    }
    if (!healAudio && !freshImageUrl) {
      logger.warn(
        { songId: song.id, refreshTaskId },
        "asset-refresh: refresh returned no usable imageUrl",
      );
      return null;
    }

    await prisma.song.update({
      where: { id: song.id },
      data: {
        ...(healAudio && fresh?.audioUrl
          ? {
              audioUrl: fresh.audioUrl,
              // Permanent-host (cdn1) URLs never expire — stamp null so the
              // TTL column says "permanent" instead of a synthetic deadline.
              audioUrlExpiresAt: isPermanentCdnUrl(fresh.audioUrl) ? null : expiresAt,
            }
          : {}),
        ...(freshImageUrl
          ? { imageUrl: freshImageUrl, imageUrlExpiresAt: expiresAt }
          : {}),
      },
    });

    // Countable heal signal: every successful heal means a stored CDN URL had
    // gone stale/dead — N heals per day in GlitchTip is the early warning for
    // the next aggregator host death (incident 2026-07-17: 114 silent heals).
    const healedAudioUrl = healAudio ? fresh?.audioUrl || undefined : undefined;
    logServerError("song-cdn-heal", new Error("song cdn urls healed"), {
      route: "lib/songs/asset-refresh",
      params: {
        songId: song.id,
        sunoJobId: refreshTaskId,
        healedAudio: !!healedAudioUrl,
        healedImage: !!freshImageUrl,
      },
      tags: { host: urlHost(healedAudioUrl ?? freshImageUrl) },
    });

    return {
      ...(healAudio && fresh?.audioUrl ? { audioUrl: fresh.audioUrl } : {}),
      ...(freshImageUrl ? { imageUrl: freshImageUrl } : {}),
    };
  } catch (err) {
    logger.warn({ songId: song.id, refreshTaskId, err }, "asset-refresh: refresh failed");
    return null;
  }
}

export interface DerivedCdnAudio {
  /** The permanent cdn1.suno.ai mp3 URL that answered. */
  url: string;
  /** The successful (`ok`) upstream response; body not yet consumed. */
  response: Response;
}

/**
 * Last resort: Suno's permanent per-clip CDN, derived from the clip id.
 * Aggregator hosts (tempfile.*) expire their files and the aggregator's
 * record-info refresh can return the same dead URL; the cdn1 mp3 outlives
 * both (incident 2026-07-17: 114 songs with dead tempfile URLs, all live on
 * cdn1). On success the row is healed so future requests skip the dead
 * origin; a failed heal write is non-fatal — the audio still streams.
 *
 * Returns null when there is no clip id, `currentUrl` already is the derived
 * URL (nothing new to try), or the derived URL is dead too.
 */
export async function fetchDerivedCdnAudio(
  song: { id: string; sunoAudioId: string | null },
  currentUrl?: string | null,
): Promise<DerivedCdnAudio | null> {
  if (!song.sunoAudioId) return null;
  const derived = sunoCdnAudioUrl(song.sunoAudioId);
  if (derived === currentUrl) return null;

  try {
    const response = await fetch(derived);
    if (!response.ok) return null;
    // Countable fallback signal: the stored origin is dead and only the
    // derived permanent URL answered — the exact event that spiked during
    // incident 2026-07-17, now visible in GlitchTip instead of stdout-only.
    logServerError(
      "audio-derived-cdn-fallback",
      new Error("audio derived-cdn fallback fired"),
      {
        route: "lib/songs/asset-refresh",
        params: { songId: song.id, deadUrl: currentUrl ?? undefined },
        tags: { host: PERMANENT_CDN_HOST, deadHost: urlHost(currentUrl) },
      },
    );
    await prisma.song
      .update({
        where: { id: song.id },
        data: {
          audioUrl: derived,
          // Derived URL is on the permanent host — null marks it never-expiring.
          audioUrlExpiresAt: null,
        },
      })
      .catch((err: unknown) => {
        logger.warn({ songId: song.id, err }, "asset-refresh: derived-cdn heal failed");
      });
    return { url: derived, response };
  } catch (err) {
    logger.warn({ songId: song.id, err }, "asset-refresh: derived cdn fetch threw");
    return null;
  }
}
